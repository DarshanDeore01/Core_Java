package SUPER10Prog.Q10;

public class Operations extends Calculator
{
	
    public int add(int a, int b)
    {
        return a + b;
    }

   
    public int add(int a, int b, int c) 
    {
        return a + b + c;
    }

    
    public float add(float a, float b)
    {
        return a + b;
    }
}
